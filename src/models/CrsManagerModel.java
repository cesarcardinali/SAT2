package models;


import customobjects.CrItem;
import customobjects.CrItemsList;


public class CrsManagerModel
{
	// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// Variables -------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	private String      CRs[];
	private String      labels[];
	private int         errors;
	private CrItemsList ignoredList;
	
	// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// Constructor -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public CrsManagerModel()
	{
		
	}
	
	// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// Model data IO ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public int getCRsCount() // getCRsCount()
	{
		return CRs.length;
	}
	
	public String getCrAt(int index)
	{
		return CRs[index];
	}
	
	public void resetErrorsCount()
	{
		errors = 0;
	}
	
	public void incrementErrorsCount()
	{
		errors++;
	}
	
	public void addToIgnoredList(CrItem cr)
	{
		ignoredList.add(cr);
	}
	
	public int getIgnoredListSize()
	{
		return ignoredList.size();
	}
	
	// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// Getters ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public String[] getCRs()
	{
		return CRs;
	}
	
	public String[] getLabels()
	{
		return labels;
	}
	
	public int getErrors()
	{
		return errors;
	}

	public CrItemsList getIgnoredList()
	{
		return ignoredList;
	}
	
	// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// Setters ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public void setCRs(String[] cRs)
	{
		CRs = cRs;
	}
	
	public void setLabels(String[] labels)
	{
		this.labels = labels;
	}
	
	public void setErrors(int errors)
	{
		this.errors = errors;
	}
	
	public void setIgnoredList(CrItemsList ignoredList)
	{
		this.ignoredList = ignoredList;
	}
}
